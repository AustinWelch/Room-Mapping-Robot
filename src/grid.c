uint32_t blockSize = 10;
robotPos robotPosition = { .direction = 0, .coordinates = { .x = 0, .y = 0 }};

robotPos Grid_getRobotPos() {
    return robotPosition;
}

void Grid_setRobotPos(robotPos newPos){
    robotPosition.coordinates.x = newPos.coordinates.x;
    robotPosition.coordinates.y = newPos.coordinates.y;
    robotPosition.direction = newPos.direction % 360;
}

// cos(x-90) == sin(x)
coord Grid_convertDistanceToCoordinate(int32_t distance, uint32_t sensor) {
    uint32_t angle;
    if (sensor == LEFT)
        angle = (robotPosition.direction + 305) % 360;
    else if (sensor == CENTER) 
        angle = robotPosition.direction;
    else // RIGHT
        angle = (robotPosition.direction + 55) % 360;
        
    uint32_t adjustedAngle = (angle + 270) % 360;
    coord location;
    location.y = (robotPosition.coordinates.y + ((distance * cos_LUT[angle]) / 10)) / (blockSize * 10000);
    location.x = (robotPosition.coordinates.x + ((distance * cos_LUT[adjustedAngle]) / 10)) / (blockSize * 10000);

    return location;
}

uint32_t Grid_getBlockSize(){
    return blockSize;
}

void Grid_setBlockSize(uint32_t newSize){
    blockSize = newSize;
}

